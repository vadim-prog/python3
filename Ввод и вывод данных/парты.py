a = int(input('Введите кол-во учеников в 1 классе: '))
b = int(input('Введите кол-во учеников в 2 классе: '))
c = int(input('Введите кол-во учеников в 3 классе: '))

if a%2 != 0:
    x1 = (a // 2) + 1
else:
    x1=a/2

if b%2 != 0:
    x2 = (b//2) +1
else:
    x2 = b / 2

if c%2 != 0:
    x3 = (c//2) +1
else:
    x3 = c / 2

print(int(x1+x2+x3))